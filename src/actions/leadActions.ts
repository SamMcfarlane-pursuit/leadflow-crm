export async function updateLeadStage(leadId: string, stage: string) {
    try {
        const userId = await requireAuth();

        // Validate stage to prevent injection of arbitrary values
        const validStages = ['New', 'Engagement', 'Proposal', 'Negotiation', 'Closed'];
        if (!validStages.includes(stage)) {
            return { success: false, error: 'Invalid pipeline stage' };
        }

        // Ensure the lead belongs to this user (prevents lateral access)
        const lead = await prisma.lead.findFirst({
            where: { id: leadId, ...userScope(userId) },
        });
        if (!lead) {
            return { success: false, error: 'Lead not found' };
        }

        await prisma.lead.update({
            where: { id: leadId },
            data: {
                pipelineStage: stage,
                logs: {
                    create: {
                        level: 'INFO',
                        message: `Lead moved to ${stage}`,
                        module: 'CORE'
                    }
                }
            }
        });
        revalidatePath('/pipeline');
        revalidatePath('/dashboard');
        return { success: true };
    } catch (error) {
        console.error('Failed to update lead stage:', error);
        return { success: false, error: 'Failed to update lead stage' };
    }
}

// --- RESCORE: Recalculate all leads with unified scoring ---
export async function rescoreAllLeads() {
    const userId = await requireAuth();
    const { calculateTier } = await import('@/utils/ironGateLogic');

    // Only rescore this user's leads
    const leads = await prisma.lead.findMany({
        where: userScope(userId),
        select: { id: true, revenue: true },
    });
    let updated = 0;

    const CHUNK_SIZE = 50;
    for (let i = 0; i < leads.length; i += CHUNK_SIZE) {
        const chunk = leads.slice(i, i + CHUNK_SIZE);
        await prisma.$transaction(
            chunk.map(lead => {
                const { tier, temperature, score } = calculateTier(lead.revenue);
                return prisma.lead.update({
                    where: { id: lead.id },
                    data: { tier, temperature, score },
                });
            })
        );
        updated += chunk.length;
    }

    revalidatePath('/dashboard');
    revalidatePath('/pipeline');
    revalidatePath('/analytics');
    return { updated };
}
